import { User } from 'interfaces/User'

async function getUsersData() {
  const users = await fetch('http://localhost:3000/api/user', {
    method: 'GET',
    headers: {
      Accept: 'application/json',
      'Content-Type': 'application/json',
    },
  }).then((res) => res.json())

  return users
}

